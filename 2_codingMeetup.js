function greetDevelopers(list) {

  for (let i=0; i < list.length; i++) {
    let obj = list[i];
    let sentence = 'Hi ' + obj.firstName +  ', what do you like the most about ' + obj.language + '?'
    obj['greeting'] = sentence
  }

  return list
}